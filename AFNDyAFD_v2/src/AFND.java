    private void btnGenerarTablaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarTablaActionPerformed
        alfabeto=txtAlfabeto.getText().split(",");
        estados=txtEstados.getText().split(",");
        System.out.println("Alfbeto length:"+alfabeto.length);
        System.out.println("Estados length:"+estados.length);
        modelTransiciones.addColumn("Estados/Alfabeto");
        for(int i=0; i<alfabeto.length-1;i++){
            modelTransiciones.addColumn(alfabeto[i]);        
        }
        for(int i=0; i<estados.length-1;i++){
            modelTransiciones.addRow(new Object[]{estados[i],alfabeto});
        }
    }//GEN-LAST:event_btnGenerarTablaActionPerformed

